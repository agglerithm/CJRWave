public enum SupportedWaveFormat
{
    WAVE_FORMAT_1M08 = 1,
    WAVE_FORMAT_1S08 = 2,
    WAVE_FORMAT_1M16 = 4,
    WAVE_FORMAT_1S16 = 8,
    WAVE_FORMAT_2M08 = 16, // 0x00000010
    WAVE_FORMAT_2S08 = 32, // 0x00000020
    WAVE_FORMAT_2M16 = 64, // 0x00000040
    WAVE_FORMAT_2S16 = 128, // 0x00000080
    WAVE_FORMAT_4M08 = 256, // 0x00000100
    WAVE_FORMAT_4S08 = 512, // 0x00000200
    WAVE_FORMAT_4M16 = 1024, // 0x00000400
    WAVE_FORMAT_4S16 = 2048, // 0x00000800
    WAVE_FORMAT_44M08 = WAVE_FORMAT_4M08, // 0x00000100
    WAVE_FORMAT_44S08 = WAVE_FORMAT_4S08, // 0x00000200
    WAVE_FORMAT_44M16 = WAVE_FORMAT_4M16, // 0x00000400
    WAVE_FORMAT_44S16 = WAVE_FORMAT_4S16, // 0x00000800
    WAVE_FORMAT_48M08 = 4096, // 0x00001000
    WAVE_FORMAT_48S08 = 8192, // 0x00002000
    WAVE_FORMAT_48M16 = 16384, // 0x00004000
    WAVE_FORMAT_48S16 = 32768, // 0x00008000
    WAVE_FORMAT_96M08 = 65536, // 0x00010000
    WAVE_FORMAT_96S08 = 131072, // 0x00020000
    WAVE_FORMAT_96M16 = 262144, // 0x00040000
    WAVE_FORMAT_96S16 = 524288, // 0x00080000
}